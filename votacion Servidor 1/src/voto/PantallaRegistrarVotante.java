    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Boolean guardar = true;
        String mensaje = "";
        //int DNI = Integer.parseInt(DNI);
        int DNI = Integer.parseInt(txtDNI.getText());
        String nombre = txtNombre.getText().toUpperCase();
        String AP = txtAP.getText().toUpperCase();
        String AM = txtAM.getText().toUpperCase();
        String direccion = txtDireccion.getText().toUpperCase();
        String voto = txtVoto.getText();
        
        if(DNI == 0 || txtDNI.getText().length()>8){
            guardar=false;
            mensaje+="Escriba un DNI VALIDA\n";
        }        
        if(nombre.isEmpty()){
            guardar = false;
            mensaje+="Escriba el nombre, por favor\n";
        }
        if(AP.isEmpty()){
            guardar = false;
            mensaje+="Escriba el nombre, por favor\n";
         }
        if(AM.isEmpty()){
            guardar = false;
            mensaje+="Escriba el nombre, por favor\n";
         }
        if(direccion.isEmpty()){
            guardar = false;
            mensaje+="Escriba el nombre, por favor\n";
         }       
         if(guardar){
           ControlDatos control = new ControlDatos();
           control.insertar(DNI,nombre, AP, AM, direccion, voto);
           
           txtDNI.setText("");
           txtNombre.setText("");
           txtAP.setText("");
           txtAM.setText("");
           txtDireccion.setText("");
           txtVoto.setText("0");
           
        }
        else JOptionPane.showMessageDialog(null, mensaje);
    }//GEN-LAST:event_btnRegistrarActionPerformed

